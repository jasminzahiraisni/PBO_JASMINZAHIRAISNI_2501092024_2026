   public static void main(String[] args) {
        
        String[] days = {"Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"};
        
        int j = 0;
        while (j < days.length) {
            System.out.println(days[j]);
            j++;
        }
   
        System.out.println(); 
        
        
        int k = 0;
        do {
            System.out.println(days[k]);
            k++;
        } while (k < days.length);
          k++;
          
    }
